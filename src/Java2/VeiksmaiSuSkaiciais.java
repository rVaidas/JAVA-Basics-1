public class VeiksmaiSuSkaiciais {
    public static void main(String[] args) {
        int ats = 6 / 4;
        System.out.println(ats);
//        palieka tik sveikaja dali

        double ats2 = 7. / 4;
        System.out.println(ats2);

        int didinam = 2;
        System.out.println(didinam++);
        System.out.println(didinam);
        System.out.println(++didinam);
        System.out.println(didinam++);
        System.out.println(didinam + 1);

//        ta pati galima daryti su minusais -
    }
}
